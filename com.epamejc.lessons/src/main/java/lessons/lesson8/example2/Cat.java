package lessons.lesson8.example2;

import lombok.Data;

@Data
public class Cat {

  private Integer id;
  private String name;

  public Cat(Integer id, String name) {
    this.id = id;
    this.name = name;
  }

  public Integer getId() {
    return id;
  }

  public void setId(Integer id) {
    this.id = id;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

}
